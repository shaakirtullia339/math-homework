public class MathHomework {
    public static void main(String[] args) {
        // Example 1: Add two numbers using addition operator
        int num1 = 5;
        int num2 = 3;
        int sum = num1 + num2;
        System.out.println("Sum of " + num1 + " and " + num2 + " is: " + sum);

        // Example 2: Subtract two numbers using subtraction operator
        double difference = 7.5 - 2.0;
        System.out.println("Difference between 7.5 and 2.0 is: " + difference);
    }
}
